package com.example.android20170506_6;

public class User {
	private String name;
	private String qq;

	public User(String name, String qq) {
		this.name = name;
		this.qq = qq;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getQq() {
		return qq;
	}

	public void setQq(String qq) {
		this.qq = qq;
	}

}
